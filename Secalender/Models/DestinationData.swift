    /// 搜索国家（支持简体、繁体、英语）
    /// - Parameter searchTerm: 搜索关键词
    /// - Returns: 匹配的国家列表（按字母排序）
    public func searchCountries(_ searchTerm: String) -> [String] {
        guard !searchTerm.isEmpty else {
            return getAllCountries()
